public class YelekDTO {
	private long id;
	private long dressModelId;
	private long basketId;
	private int gogus;
	private int gobek;
	private int ayna;
	private int boy;
	
	public long getId() {
		return id;
	}
	public void setId(long id) {
		this.id = id;
	}
	public long getDressModelId() {
		return dressModelId;
	}
	public void setDressModelId(long dressModelId) {
		this.dressModelId = dressModelId;
	}
	public long getBasketId() {
		return basketId;
	}
	public void setBasketId(long basketId) {
		this.basketId = basketId;
	}
	public int getGogus() {
		return gogus;
	}
	public void setGogus(int gogus) {
		this.gogus = gogus;
	}
	public int getGobek() {
		return gobek;
	}
	public void setGobek(int gobek) {
		this.gobek = gobek;
	}
	public int getAyna() {
		return ayna;
	}
	public void setAyna(int ayna) {
		this.ayna = ayna;
	}
	public int getBoy() {
		return boy;
	}
	public void setBoy(int boy) {
		this.boy = boy;
	}
	
}
