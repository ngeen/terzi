package com.oz.tailor.DTO;

public class PantolonDTO {
	private long id;
	private long dressModelId;
	private long basketId;
	private int bel;
	private int basen;
	private int ag;
	private int dizGenisligi;
	private int paca;
	private int kavala;
	
	public long getId() {
		return id;
	}
	public void setId(long id) {
		this.id = id;
	}
	public long getDressModelId() {
		return dressModelId;
	}
	public void setDressModelId(long dressModelId) {
		this.dressModelId = dressModelId;
	}
	public long getBasketId() {
		return basketId;
	}
	public void setBasketId(long basketId) {
		this.basketId = basketId;
	}
	public int getBel() {
		return bel;
	}
	public void setBel(int bel) {
		this.bel = bel;
	}
	public int getBasen() {
		return basen;
	}
	public void setBasen(int basen) {
		this.basen = basen;
	}
	public int getAg() {
		return ag;
	}
	public void setAg(int ag) {
		this.ag = ag;
	}
	public int getDizGenisligi() {
		return dizGenisligi;
	}
	public void setDizGenisligi(int dizGenisligi) {
		this.dizGenisligi = dizGenisligi;
	}
	public int getPaca() {
		return paca;
	}
	public void setPaca(int paca) {
		this.paca = paca;
	}
	public int getKavala() {
		return kavala;
	}
	public void setKavala(int kavala) {
		this.kavala = kavala;
	}
	
}
